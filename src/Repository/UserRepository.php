class UserRepository extends ServiceEntityRepository
{
    /**
     * Constructor that initializes the repository with the entity manager and entity class.
     */
    public function __construct(ManagerRegistry $registry)
    {
        parent::__construct($registry, User::class);
    }

    /**
     * Method for saving a User entity to the database.
     *
     * @param User $entity the User entity to be saved
     * @param bool $flush  whether to flush the changes to the database immediately
     */
    public function save(User $entity, bool $flush = false): void
    {
        $this->getEntityManager()->persist($entity);

        if ($flush) {
            $this->getEntityManager()->flush();
        }
    }

    /**
     * Method for removing a User entity from the database.
     *
     * @param User $entity the User entity to be removed
     * @param bool $flush  whether to flush the changes to the database immediately
     */
    public function remove(User $entity, bool $flush = false): void
    {
        $this->getEntityManager()->remove($entity);

        if ($flush) {
            $this->getEntityManager()->flush();
        }
    }

